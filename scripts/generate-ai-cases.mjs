import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { parseMarkdownFrontmatter, requireDate, requireFields, requireStringArray } from './frontmatter.mjs'

const CONTENT_DIR = new URL('../content/ai-cases/', import.meta.url)
const OUTPUT_URL = new URL('../src/data/generatedAiCases.ts', import.meta.url)
const STAGES = ['building', 'verified-local', 'operational']
const REQUIRED_FIELDS = [
  'id', 'slug', 'title', 'stage', 'updatedAt', 'summary', 'currentFocus', 'flow', 'responsibilities',
  'evidence', 'failureHandling', 'knownLimits', 'targetOutcome', 'nextMilestone', 'relatedArticleSlugs',
]
const ARRAY_FIELDS = ['flow', 'responsibilities', 'evidence', 'failureHandling', 'knownLimits', 'relatedArticleSlugs']

if (!existsSync(CONTENT_DIR)) throw new Error('content/ai-cases does not exist.')

function parseCase(fileName) {
  const raw = readFileSync(new URL(fileName, CONTENT_DIR), 'utf8')
  const { meta } = parseMarkdownFrontmatter(raw, fileName)
  requireFields(meta, REQUIRED_FIELDS, fileName)
  ARRAY_FIELDS.forEach(field => requireStringArray(meta, field, fileName))
  requireDate(meta.updatedAt, 'updatedAt', fileName)
  if (!STAGES.includes(meta.stage)) throw new Error(`${fileName}: unknown stage ${meta.stage}.`)

  return {
    id: Number(meta.id),
    slug: String(meta.slug),
    title: String(meta.title),
    stage: String(meta.stage),
    updatedAt: String(meta.updatedAt),
    summary: String(meta.summary),
    currentFocus: String(meta.currentFocus),
    flow: meta.flow.map(String),
    responsibilities: meta.responsibilities.map(String),
    evidence: meta.evidence.map(String),
    failureHandling: meta.failureHandling.map(String),
    knownLimits: meta.knownLimits.map(String),
    targetOutcome: String(meta.targetOutcome),
    nextMilestone: String(meta.nextMilestone),
    relatedArticleSlugs: meta.relatedArticleSlugs.map(String),
  }
}

const aiCases = readdirSync(CONTENT_DIR).filter(file => file.endsWith('.md')).sort().map(parseCase)
const ids = new Set()
const slugs = new Set()
aiCases.forEach(item => {
  if (ids.has(item.id)) throw new Error(`Duplicate AI case id: ${item.id}`)
  if (slugs.has(item.slug)) throw new Error(`Duplicate AI case slug: ${item.slug}`)
  ids.add(item.id)
  slugs.add(item.slug)
})

const output = `/* eslint-disable */\n// Generated by scripts/generate-ai-cases.mjs. Do not edit by hand.\n\nexport type AiCaseStage = 'building' | 'verified-local' | 'operational'\n\nexport interface AiCase {\n  id: number\n  slug: string\n  title: string\n  stage: AiCaseStage\n  updatedAt: string\n  summary: string\n  currentFocus: string\n  flow: string[]\n  responsibilities: string[]\n  evidence: string[]\n  failureHandling: string[]\n  knownLimits: string[]\n  targetOutcome: string\n  nextMilestone: string\n  relatedArticleSlugs: string[]\n}\n\nexport const aiCases: AiCase[] = ${JSON.stringify(aiCases, null, 2)}\n`

mkdirSync(new URL('../src/data/', import.meta.url), { recursive: true })
writeFileSync(OUTPUT_URL, output)
console.log(`Generated ${aiCases.length} AI collaboration cases.`)
