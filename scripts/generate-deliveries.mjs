import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { parseMarkdownFrontmatter, requireDate, requireFields, requireStringArray } from './frontmatter.mjs'

const CONTENT_DIR = new URL('../content/deliveries/', import.meta.url)
const OUTPUT_URL = new URL('../src/data/generatedDeliveries.ts', import.meta.url)
const STATUSES = ['in-progress', 'partial', 'delivered']
const KINDS = ['engineering', 'content', 'automation']
const REQUIRED = ['slug', 'title', 'date', 'status', 'kind', 'summary', 'projectSlugs', 'goal', 'scope', 'aiContribution', 'humanDecisions', 'reviewFindings', 'corrections', 'evidenceSlugs', 'publicLinks', 'knownLimits', 'nextStep', 'publish']
const ARRAYS = ['projectSlugs', 'scope', 'aiContribution', 'humanDecisions', 'reviewFindings', 'corrections', 'evidenceSlugs', 'knownLimits']

if (!existsSync(CONTENT_DIR)) throw new Error('content/deliveries does not exist.')

function parseDelivery(fileName) {
  const { meta } = parseMarkdownFrontmatter(readFileSync(new URL(fileName, CONTENT_DIR), 'utf8'), fileName)
  requireFields(meta, REQUIRED, fileName)
  ARRAYS.forEach(field => requireStringArray(meta, field, fileName))
  requireDate(meta.date, 'date', fileName)
  if (meta.publish !== true) throw new Error(`${fileName}: public delivery records must set publish to true.`)
  if (!STATUSES.includes(meta.status)) throw new Error(`${fileName}: unknown status ${meta.status}.`)
  if (!KINDS.includes(meta.kind)) throw new Error(`${fileName}: unknown kind ${meta.kind}.`)
  if (!Array.isArray(meta.publicLinks)) throw new Error(`${fileName}: publicLinks must be an array.`)
  const publicLinks = meta.publicLinks.map((link, index) => {
    if (!link || typeof link !== 'object' || !link.label || !String(link.url || '').startsWith('https://')) {
      throw new Error(`${fileName}: publicLinks[${index}] must contain label and https URL.`)
    }
    return { label: String(link.label), url: String(link.url) }
  })
  if (meta.status === 'delivered' && (!meta.evidenceSlugs.length || !publicLinks.length)) {
    throw new Error(`${fileName}: delivered records require evidence and a real public link.`)
  }
  const serialized = JSON.stringify(meta)
  if (/\/(?:Users|home)\//.test(serialized) || /(?:gh[pousr]_|AKIA)[A-Za-z0-9_]{12,}/.test(serialized)) {
    throw new Error(`${fileName}: delivery contains a local path or credential-shaped value.`)
  }
  return {
    slug: String(meta.slug), title: String(meta.title), date: String(meta.date), status: String(meta.status), kind: String(meta.kind),
    summary: String(meta.summary), projectSlugs: meta.projectSlugs.map(String), goal: String(meta.goal), scope: meta.scope.map(String),
    aiContribution: meta.aiContribution.map(String), humanDecisions: meta.humanDecisions.map(String), reviewFindings: meta.reviewFindings.map(String),
    corrections: meta.corrections.map(String), evidenceSlugs: meta.evidenceSlugs.map(String), publicLinks,
    knownLimits: meta.knownLimits.map(String), nextStep: String(meta.nextStep), publish: true,
  }
}

const deliveries = readdirSync(CONTENT_DIR).filter(file => file.endsWith('.md')).sort().map(parseDelivery).sort((a, b) => b.date.localeCompare(a.date))
const slugs = new Set()
deliveries.forEach(item => {
  if (slugs.has(item.slug)) throw new Error(`Duplicate delivery slug: ${item.slug}`)
  slugs.add(item.slug)
})

const output = `/* eslint-disable */\n// Generated by scripts/generate-deliveries.mjs. Do not edit by hand.\n\nexport type DeliveryStatus = 'in-progress' | 'partial' | 'delivered'\nexport type DeliveryKind = 'engineering' | 'content' | 'automation'\nexport interface DeliveryLink { label: string; url: string }\nexport interface DeliveryRecord {\n  slug: string\n  title: string\n  date: string\n  status: DeliveryStatus\n  kind: DeliveryKind\n  summary: string\n  projectSlugs: string[]\n  goal: string\n  scope: string[]\n  aiContribution: string[]\n  humanDecisions: string[]\n  reviewFindings: string[]\n  corrections: string[]\n  evidenceSlugs: string[]\n  publicLinks: DeliveryLink[]\n  knownLimits: string[]\n  nextStep: string\n  publish: true\n}\nexport const deliveryRecords: DeliveryRecord[] = ${JSON.stringify(deliveries, null, 2)}\n`
mkdirSync(new URL('../src/data/', import.meta.url), { recursive: true })
writeFileSync(OUTPUT_URL, output)
console.log(`Generated ${deliveries.length} AI delivery records.`)
