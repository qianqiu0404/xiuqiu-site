import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { parseMarkdownFrontmatter, requireFields, requireStringArray } from './frontmatter.mjs'

const CONTENT_DIR = new URL('../content/failure-cases/', import.meta.url)
const OUTPUT_URL = new URL('../src/data/generatedFailureCases.ts', import.meta.url)
const CATEGORIES = ['request-state', 'node-scanning', 'deposit', 'withdrawal-finality', 'multi-chain', 'risk-signing', 'funds-operations']
const PRIORITIES = ['must-answer', 'common', 'advanced']
const EVIDENCE_STATUSES = ['implemented', 'partial', 'design']
const ACTIONS = ['retry', 'compensate', 'pause', 'manual-review']
const REQUIRED = ['slug', 'title', 'category', 'priority', 'services', 'chains', 'actions', 'evidenceStatus', 'symptom', 'fundRisk', 'stopLoss', 'investigate', 'recovery', 'idempotencyBasis', 'currentBoundary', 'verificationEvidence', 'relatedArticleSlugs', 'relatedProjectSlugs']
const ARRAYS = ['services', 'chains', 'actions', 'investigate', 'recovery', 'verificationEvidence', 'relatedArticleSlugs', 'relatedProjectSlugs']

if (!existsSync(CONTENT_DIR)) throw new Error('content/failure-cases does not exist.')

function parseCase(fileName) {
  const raw = readFileSync(new URL(fileName, CONTENT_DIR), 'utf8')
  const { meta } = parseMarkdownFrontmatter(raw, fileName)
  requireFields(meta, REQUIRED, fileName)
  ARRAYS.forEach(field => requireStringArray(meta, field, fileName))
  if (!CATEGORIES.includes(meta.category)) throw new Error(`${fileName}: unknown category ${meta.category}.`)
  if (!PRIORITIES.includes(meta.priority)) throw new Error(`${fileName}: unknown priority ${meta.priority}.`)
  if (!EVIDENCE_STATUSES.includes(meta.evidenceStatus)) throw new Error(`${fileName}: unknown evidenceStatus ${meta.evidenceStatus}.`)
  if (meta.actions.length === 0 || meta.actions.some(action => !ACTIONS.includes(action))) throw new Error(`${fileName}: invalid actions.`)
  if (meta.investigate.length === 0) throw new Error(`${fileName}: investigate must not be empty.`)
  if (meta.recovery.length === 0) throw new Error(`${fileName}: recovery must not be empty.`)
  return {
    slug: String(meta.slug), title: String(meta.title), category: String(meta.category), priority: String(meta.priority),
    services: meta.services.map(String), chains: meta.chains.map(String), actions: meta.actions.map(String),
    evidenceStatus: String(meta.evidenceStatus), symptom: String(meta.symptom), fundRisk: String(meta.fundRisk),
    stopLoss: String(meta.stopLoss), investigate: meta.investigate.map(String), recovery: meta.recovery.map(String),
    idempotencyBasis: String(meta.idempotencyBasis), currentBoundary: String(meta.currentBoundary),
    verificationEvidence: meta.verificationEvidence.map(String), relatedArticleSlugs: meta.relatedArticleSlugs.map(String),
    relatedProjectSlugs: meta.relatedProjectSlugs.map(String),
  }
}

const failureCases = readdirSync(CONTENT_DIR).filter(file => file.endsWith('.md')).sort().map(parseCase)
const slugs = new Set()
failureCases.forEach(item => {
  if (slugs.has(item.slug)) throw new Error(`Duplicate failure case slug: ${item.slug}`)
  slugs.add(item.slug)
})

const output = `/* eslint-disable */\n// Generated by scripts/generate-failure-cases.mjs. Do not edit by hand.\n\nexport type FailureCategory = ${CATEGORIES.map(v => `'${v}'`).join(' | ')}\nexport type FailurePriority = ${PRIORITIES.map(v => `'${v}'`).join(' | ')}\nexport type FailureEvidenceStatus = ${EVIDENCE_STATUSES.map(v => `'${v}'`).join(' | ')}\nexport type FailureAction = ${ACTIONS.map(v => `'${v}'`).join(' | ')}\n\nexport interface FailureCase {\n  slug: string\n  title: string\n  category: FailureCategory\n  priority: FailurePriority\n  services: string[]\n  chains: string[]\n  actions: FailureAction[]\n  evidenceStatus: FailureEvidenceStatus\n  symptom: string\n  fundRisk: string\n  stopLoss: string\n  investigate: string[]\n  recovery: string[]\n  idempotencyBasis: string\n  currentBoundary: string\n  verificationEvidence: string[]\n  relatedArticleSlugs: string[]\n  relatedProjectSlugs: string[]\n}\n\nexport const failureCases: FailureCase[] = ${JSON.stringify(failureCases, null, 2)}\n`

mkdirSync(new URL('../src/data/', import.meta.url), { recursive: true })
writeFileSync(OUTPUT_URL, output)
console.log(`Generated ${failureCases.length} failure cases.`)
