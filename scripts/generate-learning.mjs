import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { parseMarkdownFrontmatter, requireFields } from './frontmatter.mjs'

const CONTENT_DIR = new URL('../content/learning/', import.meta.url)
const OUTPUT_URL = new URL('../src/data/generatedLearningRecords.ts', import.meta.url)
const REQUIRED_FIELDS = [
  'id',
  'slug',
  'title',
  'date',
  'summary',
  'achieved',
  'evidence',
  'reflection',
  'nextSteps',
  'publish',
]

if (!existsSync(CONTENT_DIR)) throw new Error('content/learning does not exist.')

function parseRecord(fileName) {
  const raw = readFileSync(new URL(fileName, CONTENT_DIR), 'utf8')
  const { meta } = parseMarkdownFrontmatter(raw, fileName)
  requireFields(meta, REQUIRED_FIELDS, fileName)

  if (meta.publish !== true) throw new Error(`${fileName}: public learning records must set publish to true.`)
  ;['achieved', 'evidence', 'reflection', 'nextSteps'].forEach(field => {
    if (!Array.isArray(meta[field])) throw new Error(`${fileName}: ${field} must be an array.`)
  })

  return {
    id: Number(meta.id),
    slug: String(meta.slug),
    title: String(meta.title),
    date: String(meta.date),
    summary: String(meta.summary),
    projectIds: Array.isArray(meta.projectIds) ? meta.projectIds.map(Number) : [],
    achieved: meta.achieved.map(String),
    evidence: meta.evidence.map(String),
    reflection: meta.reflection.map(String),
    nextSteps: meta.nextSteps.map(String),
  }
}

const records = readdirSync(CONTENT_DIR)
  .filter(file => file.endsWith('.md'))
  .sort()
  .map(parseRecord)
  .sort((a, b) => b.date.localeCompare(a.date) || b.id - a.id)

const output = `/* eslint-disable */\n// Generated by scripts/generate-learning.mjs. Do not edit by hand.\n\nexport interface LearningRecord {\n  id: number\n  slug: string\n  title: string\n  date: string\n  summary: string\n  projectIds: number[]\n  achieved: string[]\n  evidence: string[]\n  reflection: string[]\n  nextSteps: string[]\n}\n\nexport const learningRecords: LearningRecord[] = ${JSON.stringify(records, null, 2)}\n`

mkdirSync(new URL('../src/data/', import.meta.url), { recursive: true })
writeFileSync(OUTPUT_URL, output)
console.log(`Generated ${records.length} public learning records.`)
