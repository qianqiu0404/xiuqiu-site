import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { parseMarkdownFrontmatter, requireDate, requireFields, requireStringArray } from './frontmatter.mjs'

const CONTENT_DIR = new URL('../content/evidence/', import.meta.url)
const OUTPUT_URL = new URL('../src/data/generatedEvidence.ts', import.meta.url)
const KINDS = ['implementation', 'test', 'demo', 'writeup']
const STATUSES = ['verified', 'partial', 'design']
const VISIBILITIES = ['public', 'private-summary']
const REQUIRED = ['slug', 'title', 'capabilityIds', 'projectSlugs', 'kind', 'status', 'visibility', 'summary', 'verifiedAt', 'failureSlugs', 'deliverySlugs']
const ARRAYS = ['capabilityIds', 'projectSlugs', 'failureSlugs', 'deliverySlugs']

if (!existsSync(CONTENT_DIR)) throw new Error('content/evidence does not exist.')

function assertPublicSafe(value, fileName) {
  const text = JSON.stringify(value)
  if (/\/(?:Users|home)\//.test(text) || /(?:gh[pousr]_|AKIA)[A-Za-z0-9_]{12,}/.test(text)) {
    throw new Error(`${fileName}: evidence contains a local path or credential-shaped value.`)
  }
}

function parseEvidence(fileName) {
  const { meta } = parseMarkdownFrontmatter(readFileSync(new URL(fileName, CONTENT_DIR), 'utf8'), fileName)
  requireFields(meta, REQUIRED, fileName)
  ARRAYS.forEach(field => requireStringArray(meta, field, fileName))
  requireDate(meta.verifiedAt, 'verifiedAt', fileName)
  if (!KINDS.includes(meta.kind)) throw new Error(`${fileName}: unknown kind ${meta.kind}.`)
  if (!STATUSES.includes(meta.status)) throw new Error(`${fileName}: unknown status ${meta.status}.`)
  if (!VISIBILITIES.includes(meta.visibility)) throw new Error(`${fileName}: unknown visibility ${meta.visibility}.`)
  if (meta.visibility === 'public' && (!meta.url || !String(meta.url).startsWith('https://'))) {
    throw new Error(`${fileName}: public evidence requires an https URL.`)
  }
  if (meta.visibility === 'private-summary' && meta.url) throw new Error(`${fileName}: private-summary evidence must not expose a URL.`)
  if (!meta.capabilityIds.length || !meta.projectSlugs.length) throw new Error(`${fileName}: capabilityIds and projectSlugs cannot be empty.`)
  assertPublicSafe(meta, fileName)
  return {
    slug: String(meta.slug), title: String(meta.title), capabilityIds: meta.capabilityIds.map(String), projectSlugs: meta.projectSlugs.map(String),
    kind: String(meta.kind), status: String(meta.status), visibility: String(meta.visibility), summary: String(meta.summary),
    verifiedAt: String(meta.verifiedAt), command: meta.command ? String(meta.command) : undefined, url: meta.url ? String(meta.url) : undefined,
    failureSlugs: meta.failureSlugs.map(String), deliverySlugs: meta.deliverySlugs.map(String),
  }
}

const records = readdirSync(CONTENT_DIR).filter(file => file.endsWith('.md')).sort().map(parseEvidence)
const slugs = new Set()
records.forEach(record => {
  if (slugs.has(record.slug)) throw new Error(`Duplicate evidence slug: ${record.slug}`)
  slugs.add(record.slug)
})

const output = `/* eslint-disable */\n// Generated by scripts/generate-evidence.mjs. Do not edit by hand.\n\nexport type EvidenceKind = 'implementation' | 'test' | 'demo' | 'writeup'\nexport type EvidenceStatus = 'verified' | 'partial' | 'design'\nexport type EvidenceVisibility = 'public' | 'private-summary'\n\nexport interface EvidenceRecord {\n  slug: string\n  title: string\n  capabilityIds: string[]\n  projectSlugs: string[]\n  kind: EvidenceKind\n  status: EvidenceStatus\n  visibility: EvidenceVisibility\n  summary: string\n  verifiedAt: string\n  command?: string\n  url?: string\n  failureSlugs: string[]\n  deliverySlugs: string[]\n}\n\nexport const evidenceRecords: EvidenceRecord[] = ${JSON.stringify(records, null, 2)}\n`
mkdirSync(new URL('../src/data/', import.meta.url), { recursive: true })
writeFileSync(OUTPUT_URL, output)
console.log(`Generated ${records.length} engineering evidence records.`)
